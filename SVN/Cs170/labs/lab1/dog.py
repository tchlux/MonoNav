
class Dog:

    def __init__(self, name):
        self.name = name

    def speak(self):
        print(self.name, "says Woof.")

class Labrador(Dog):

    def __init__(self, name, color):
        super().__init__(name)
        self.color = color

    def speak(self):
        print(self.name, "is a lab and is", self.color)
