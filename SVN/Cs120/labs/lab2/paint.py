 # this calculates the number of gallons of paint required
 # pre: all parameters must be real positive numbers
 # post: the output in gallons is real and positive
def calculate_paint_volume(length, width, height):
    square_feet_per_gallon = 350.0
    total_surface_area = (2 * length * width) + (2 * length * height) + (2 * width * height)
    gallons = total_surface_area / square_feet_per_gallon
    return gallons



